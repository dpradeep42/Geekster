package Nov_09_2021;

public class Assignment {
    public static void main(String[] args) {
        int n = 1;
        while(n <= 10){
            System.out.println(n);
            n++;
        }
        System.out.println("\n");
        while(n > 1){
            System.out.println(n - 1);
            n--;
        }
    }
}
